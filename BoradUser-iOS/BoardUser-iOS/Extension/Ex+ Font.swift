    enum Pretendard {
        case semibold
        case medium
        case bold
        case custom(String)
        
        var value: String {
            switch self {
            case .semibold:
                return "Pretendard-SemiBold"
            case .medium:
                return "Pretendard-Medium"
            case .bold:
                return "Pretendard-Bold"
            case .custom(let name):
                return name
            }
        }
    }
    
    static func pre(_ type: Pretendard, size: CGFloat = 17) -> Font {
        return custom(type.value , size: size)
    }
    
